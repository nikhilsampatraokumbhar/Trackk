/**
 * Export and share as PDF
 */
export async function sharePDF(transactions: Transaction[], periodLabel: string): Promise<void> {
  const html = generatePDFHTML(transactions, periodLabel);
  const { uri } = await Print.printToFileAsync({ html, base64: false });

  const isAvailable = await Sharing.isAvailableAsync();
  if (isAvailable) {
    await Sharing.shareAsync(uri, {
      mimeType: 'application/pdf',
      dialogTitle: `Trackk Report - ${periodLabel}`,
      UTI: 'com.adobe.pdf',
    });
  } else {
    await Share.share({ message: `Trackk Report saved to: ${uri}` });
  }
}

